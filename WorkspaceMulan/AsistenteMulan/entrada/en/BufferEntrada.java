package en;

import java.util.Scanner;

public class BufferEntrada {
	
	private String entrada;
	
	private static String leerBuffer() {
		Scanner sc = new Scanner(System.in);
		return sc.nextLine();
	}
	
	public BufferEntrada() {
		this.entrada = BufferEntrada.leerBuffer();	
	}

	public String getEntrada() {
		return entrada;
	}
	
	
	
	
	

}
